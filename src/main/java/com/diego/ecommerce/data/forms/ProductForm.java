package com.diego.ecommerce.data.forms;

public class ProductForm {

    /**
     * Nome do produto a ser cadastrado
     */
    public String name;

    /**
     * Descrição do produto a ser cadastrado
     */
    public String description;

    /**
     * Preço do produto
     */
    public Double price;

    /**
     * URL de acesso à imagem cadastrada
     */
    public String productImage;

    /**
     * Código da categoria do produto
     */
    public int category;

}
